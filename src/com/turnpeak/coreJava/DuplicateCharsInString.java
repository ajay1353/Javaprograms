public class DuplicateCharsInString extends AbsMethod
{

	@Override
	public int sum(final int a, final int b)
	{
		final int f = a + b;
		System.out.println(f);
		return f;
	}

	public int mul(final int x, final int y)
	{
		final int z = x * y;
		System.out.println(z);
		return z;
	}

}
